
public class Shape {

    public static void main(String[] args) {
        Shape s = new Shape();
        s.circle();
        s.square();
        s.rectangle();
        s.triangle();
    }

    public void circle() {
        System.out.println("Circle");
    }

    public void square() {
        System.out.println("Square");
    }

    public void rectangle() {
        System.out.println("Rectangle");
    }

    public void triangle() {
        System.out.println("Triangle");
    }

}
